import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOProps {
  title?: string;
  description?: string;
  keywords?: string;
  canonicalUrl?: string;
  ogImage?: string;
  ogType?: string;
  structuredData?: object;
}

const SEO: React.FC<SEOProps> = ({
  title = "Astral Bridge - Lightning Fast Cross-Chain Crypto Bridge | Solana, Ethereum, BSC",
  description = "Transfer crypto assets between Solana, Ethereum, BSC and other leading blockchains with unparalleled speed, security, and lowest fees. Experience cross-chain bridging in seconds.",
  keywords = "cross-chain bridge, crypto bridge, solana bridge, ethereum bridge, BSC bridge, defi bridge, blockchain bridge, cryptocurrency transfer, cross-chain swap, astral bridge",
  canonicalUrl = "https://astralbridge.org/",
  ogImage = "https://astralbridge.org/og-image.png",
  ogType = "website",
  structuredData
}) => {
  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{title}</title>
      <meta name="title" content={title} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <link rel="canonical" href={canonicalUrl} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content={ogType} />
      <meta property="og:url" content={canonicalUrl} />
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />
      
      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={canonicalUrl} />
      <meta property="twitter:title" content={title} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={ogImage} />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
};

export default SEO;
